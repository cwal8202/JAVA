// 자기 이름 출력하기
public class PrintName {
    public static void main(String[] args) {
        System.out.println("최 장 호");
        System.out.println("나이 : 24");
        System.out.println("키 : 183");
        System.out.println("몸무게 : 77kg");
        System.out.println("성별 : 남성");

        System.out.println("10" + "10");  //"숫자" 문자로 인식
        System.out.println("11" + "11");
        System.out.println("12" + "12");

        System.out.println(20 + 20);  //" " 없는 숫자 = 정수로 인식 즉 숫자로 인식함.
        System.out.println(21 + 21);
        System.out.println(22 + 22);


    }
}