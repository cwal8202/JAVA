// 관계연산자

public class RelOper {
	public static void main(String[] args) {
		System.out.println(55 == 55); // 이퀄이라 대입연산자가 아니라 양옆이 같냐고 물어보는것
		System.out.println(55 != 55); // !는 부정을 뜻하기 때문에 다르냐고 물어본 것
		System.out.println(55 > 55);  // 왼쪽이 더 크냐?(초과)
		System.out.println(55 < 55);  // 왼쪽이 더 작냐?(미만)
		System.out.println(55 >= 55); // 왼쪽이 더 크거나 같냐? (이상)
		System.out.println(55 <= 55); // 왼쪽이 더 작거나 같냐? (이하)
		
		
	}
}